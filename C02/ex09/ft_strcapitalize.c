char	*ft_strcapitalize(char *str)
{
	char	*aux;

	aux = str;
	if (*str >= 'a' && *str <= 'z')
		*str = *str - 32;
	str++;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
			*str = *str + 32;
		if (!(*(str - 1) >= '0' && *(str - 1) <= '9')
			&& !(*(str - 1) >= 'A' && *(str - 1) <= 'Z')
				&& !(*(str - 1) >= 'a' && *(str - 1) <= 'z')
					&& *str >= 'a' && *str <= 'z')
			*str = *str - 32;
		++str;
	}
	return (aux);
}
