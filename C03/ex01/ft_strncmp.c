int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int a;

	a = 0;
	while (s1[a] != '\0' && s2[a] != '\0' && a < n && s1[a] == s2[a])
		a++;
	if (a == n)
		return (0);
	if (s1[a] == s2[a])
		return (0);
	else
		return (s1[a] - s2[a]);
}
